/** Exercici 1

* Calcula la longitud d'una circumferència a partir del radi.

* @author Cristian

*/


package exercicis;

public class Exercici01 {

	public static void main(String[] args) {
		
		final double PI=3.14159;
		double radi=2;
		double longitud=2*PI*radi;
		System.out.println("La longitud de la circumferència és: " + longitud);
	}

}
